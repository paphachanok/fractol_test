#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*y;

	y = (char *)s;
	if (c == 0)
	{
		while (*y != '\0')
		{
			y++;
		}
		return (y);
	}
	while (*y != '\0')
	{
		if (*y == (char)c)
		{
			return (y);
		}
		y++;
	}
	return (NULL);
}
