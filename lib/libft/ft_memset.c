#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*y;

	i = 0;
	y = (unsigned char *)b;
	while (len > 0)
	{
		y[i++] = c;
		len--;
	}
	return (b = y);
}
