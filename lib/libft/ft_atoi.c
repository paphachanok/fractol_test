#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	long	nega;
	long	result;

	i = 0;
	result = 0;
	nega = 1;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		nega = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10;
		result += str[i] - 48;
		i++;
	}
	return (nega * result);
}
