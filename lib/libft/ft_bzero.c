#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*y;

	i = 0;
	y = (unsigned char *)s;
	while (n > 0)
	{
		y[i++] = '\0';
		n--;
	}
	s = y;
}
